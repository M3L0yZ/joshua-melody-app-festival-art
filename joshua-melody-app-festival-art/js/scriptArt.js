let oeuvreTitre = document.querySelector("h1");
let nomArtiste = document.querySelector("h3");
let nomArtisteModal = document.querySelector(".modal-title");
let photoArtiste = document.querySelector(".card-img-top");
let oeuvreImage = document.querySelector(".img-artwork");
let brefArtiste = document.querySelector(".description");
let infoArtiste = document.querySelector(".modal-body");
let aPropos = document.querySelector(".apropos");

function affichageOeuvre(oeuvre) {
  oeuvreTitre.textContent = oeuvres[oeuvre].titre;
  nomArtiste.textContent = oeuvres[oeuvre].artiste;
  nomArtisteModal.textContent = oeuvres[oeuvre].artiste;
  brefArtiste.textContent = oeuvres[oeuvre].brefArtiste;
  infoArtiste.textContent = oeuvres[oeuvre].infoArtiste;
  photoArtiste.src = oeuvres[oeuvre].imageArtiste;
  oeuvreImage.style.backgroundImage = oeuvres[oeuvre].imageOeuvre;
  aPropos.textContent = oeuvres[oeuvre].aPropos;
}

function detectionOeuvre(id) {
  localStorage.setItem("oeuvre", id);
}

let numOeuvre = localStorage.getItem("oeuvre");
affichageOeuvre(numOeuvre);

function supprimerStorage() {
  localStorage.clear();
}


